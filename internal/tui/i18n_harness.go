// ─── i18n 子模块：Harness ───
//
// 规范说明
//
// 1. 命名约定：文件名 i18n_<module>.go，函数名 en<harness>/zh<harness>Module
// 2. 每个子模块文件只做一件事：返回 map[string]string
// 3. 新增模块时：
//    a. 创建 i18n_<module>.go，实现 enXxxModule() / zhXxxModule()
//    b. 在 init() 中调用 registerCatalog(enXxxModule(), zhXxxModule())
//    c. 在本项目目录下执行 go build 验证编译通过
// 4. key 命名：<module>.<section>.<detail>，例如 harness.hints.move
// 5. en/zh 两个 map 的 key 集合必须完全一致

